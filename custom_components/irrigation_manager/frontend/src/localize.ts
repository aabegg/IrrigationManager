export type MessageKey = keyof (typeof messages)["en"];

export function localize(hass: Pick<HomeAssistant, "language">, key: MessageKey): string {
  const language = hass.language?.toLowerCase().startsWith("de") ? "de" : "en";
  return messages[language][key];
}

export function translatedValue(hass: Pick<HomeAssistant, "language">, value: string): string {
  return value in messages.en ? localize(hass, value as MessageKey) : value.replaceAll("_", " ");
}

export function displayState(hass: HomeAssistant, entity?: HassEntity): string {
  if (!entity) return localize(hass, "missing");
  if (entity.state === "unavailable") return localize(hass, "unavailable");
  if (entity.state === "unknown" || entity.state === "") return localize(hass, "unknown");
  if (hass.formatEntityState) return hass.formatEntityState(entity);
  const unit = entity.attributes.unit_of_measurement;
  return `${translatedValue(hass, entity.state)}${unit ? ` ${unit}` : ""}`;
}
